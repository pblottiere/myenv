#-------------------------------------------------------------------------------
# imports
#-------------------------------------------------------------------------------
import os
import sys
import fileinput

sys.path.insert(0, "../scripts")
import confoption

#-------------------------------------------------------------------------------
# const, paths, ...
#-------------------------------------------------------------------------------

HOME=".fluxbox"
KEYS="keys"

#===============================================================================
#
# Functions
#
#===============================================================================
#-------------------------------------------------------------------------------
# home
#-------------------------------------------------------------------------------
def home():
    home = os.path.expanduser("~")
    return os.path.join(home, HOME)

#-------------------------------------------------------------------------------
# keys
#-------------------------------------------------------------------------------
def keys():
    return os.path.join(home(), KEYS)

#-------------------------------------------------------------------------------
# part_create
#-------------------------------------------------------------------------------
def part_create():
    confoption.part_create(keys(), "MYENV", begin=True)

#-------------------------------------------------------------------------------
# part_add
#-------------------------------------------------------------------------------
def part_add(data):
    confoption.part_add(keys(), "MYENV", data)

#-------------------------------------------------------------------------------
# part_delete
#-------------------------------------------------------------------------------
def part_delete():
    confoption.part_delete(keys(), "MYENV")
