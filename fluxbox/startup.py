#-------------------------------------------------------------------------------
# imports
#-------------------------------------------------------------------------------
import os
import sys
import fileinput

sys.path.insert(0, "../scripts")
import confoption

#-------------------------------------------------------------------------------
# const, paths, ...
#-------------------------------------------------------------------------------

HOME=".fluxbox"
STARTUP="startup"

#===============================================================================
#
# Functions
#
#===============================================================================
#-------------------------------------------------------------------------------
# home
#-------------------------------------------------------------------------------
def home():
    home = os.path.expanduser("~")
    return os.path.join(home, HOME)

#-------------------------------------------------------------------------------
# startup
#-------------------------------------------------------------------------------
def startup():
    return os.path.join(home(), STARTUP)

#-------------------------------------------------------------------------------
# part_create
#-------------------------------------------------------------------------------
def part_create():
    confoption.part_create(startup(), "MYENV", before="exec fluxbox\n")

#-------------------------------------------------------------------------------
# part_add
#-------------------------------------------------------------------------------
def part_add(data):
    confoption.part_add(startup(), "MYENV", data)

#-------------------------------------------------------------------------------
# part_delete
#-------------------------------------------------------------------------------
def part_delete():
    confoption.part_delete(startup(), "MYENV")
